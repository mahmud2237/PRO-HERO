const friends = ['bani', 'honey', 'money', 'pani', 'kani'];
console.log("Friends are Array:", Array.isArray(friends));

const numbers = [1, 2, 3, 4, 5];
console.log("Numbers are Array:", Array.isArray(numbers));

const mixed = ['bani', 1, 'money', 3.14, true]; 
console.log("Mixed are Array:", Array.isArray(mixed));

const emptyArray = [];
console.log("Empty Array:", emptyArray.length);
